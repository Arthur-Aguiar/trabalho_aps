//Uso de constante para armazenar um valor matemático:

const PI = 3.14159;
const raio = 5;
const area = PI * raio * raio;
console.log("Área do círculo:", area);

//Nesse exemplo,a constante PI é usada para armazenar o valor de π (pi), que é uma constante matemática. 
//Em seguida, ela é utilizada junto com uma variável raio para calcular a área de um círculo.