//Uso de constante para armazenar um valor fixo:

const LIMITE_IDADE = 18;
let idade = 16;
let podeVotar = idade >= LIMITE_IDADE;
console.log("Pode votar?", podeVotar);


//Neste exemplo, a constante LIMITE_IDADE é usada para armazenar o valor fixo de 18, 
//que é o limite de idade para votar em alguns países. Em seguida, ela é usada para 
//verificar se uma pessoa pode votar com base em sua idade